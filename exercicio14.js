// Declarando as variáveis 'num1' e 'num2' e pedindo ao usuário para atribuir valores a elas
let num1 = parseFloat(prompt ("Digite o primeiro número:"))
let num2 = parseFloat(prompt ("Digite o segundo número:"))

// Calculando a soma dos dois números e armazenando o resultado na variável 'soma'
let soma = (num1 + num2)

// Exibindo o resultado da soma
alert (`A soma de ${num1} e ${num2} é igual a ${soma}.`)